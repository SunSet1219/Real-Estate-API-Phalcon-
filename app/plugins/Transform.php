<?
class Transform
{
    public static function time($time){
        $hours = floor($time / 60);
        $minutes = ($time % 60);
        if($hours<10)
            $hours = "0".$hours;
        if($minutes<10)
            $minutes = "0".$minutes;
        return $hours.":".$minutes;
    }

    public static function hours($time){
        return floor($time / 60);
    }

    public static function hoursFloat($time){
        $hours = floor($time / 60);
        $minutes = ($time % 60);
        if($hours<10)
            $hours = "0".$hours;
        if($minutes<10)
            $minutes = "0".$minutes;
        return round($hours+($minutes/60), 2);
    }

    public static function translit($st){
        $converter = array(
            'а' => 'a',   'б' => 'b',   'в' => 'v',
            'г' => 'g',   'д' => 'd',   'е' => 'e',
            'ё' => 'e',   'ж' => 'zh',  'з' => 'z',
            'и' => 'i',   'й' => 'y',   'к' => 'k',
            'л' => 'l',   'м' => 'm',   'н' => 'n',
            'о' => 'o',   'п' => 'p',   'р' => 'r',
            'с' => 's',   'т' => 't',   'у' => 'u',
            'ф' => 'f',   'х' => 'h',   'ц' => 'c',
            'ч' => 'ch',  'ш' => 'sh',  'щ' => 'sch',
            'ь' => '\'',  'ы' => 'y',   'ъ' => '\'',
            'э' => 'e',   'ю' => 'yu',  'я' => 'ya',

            'А' => 'A',   'Б' => 'B',   'В' => 'V',
            'Г' => 'G',   'Д' => 'D',   'Е' => 'E',
            'Ё' => 'E',   'Ж' => 'Zh',  'З' => 'Z',
            'И' => 'I',   'Й' => 'Y',   'К' => 'K',
            'Л' => 'L',   'М' => 'M',   'Н' => 'N',
            'О' => 'O',   'П' => 'P',   'Р' => 'R',
            'С' => 'S',   'Т' => 'T',   'У' => 'U',
            'Ф' => 'F',   'Х' => 'H',   'Ц' => 'C',
            'Ч' => 'Ch',  'Ш' => 'Sh',  'Щ' => 'Sch',
            'Ь' => '\'',  'Ы' => 'Y',   'Ъ' => '\'',
            'Э' => 'E',   'Ю' => 'Yu',  'Я' => 'Ya',
        );
        $st = strtr($st, $converter);
        $str = strtolower($st);
        $str = preg_replace('~[^-a-z0-9_.]+~u', '-', $str);
        return $str;
    }
}
